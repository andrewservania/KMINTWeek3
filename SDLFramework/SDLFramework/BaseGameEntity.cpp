#include "BaseGameEntity.h"
/// <summary>	The next valid identifier for the base game entity. </summary>
int BaseGameEntity::nextValidID = 0;

////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>	Constructor. </summary>
/// Create a base game entity by providing it an id.
///
/// <remarks>	Andrew Servania,. </remarks>
///
/// <param name="id">	The identifier. </param>
////////////////////////////////////////////////////////////////////////////////////////////////////

BaseGameEntity::BaseGameEntity(int id)
{
	SetID(id);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>	Destructor. </summary>
///
/// <remarks>	Andrew Servania,. </remarks>
////////////////////////////////////////////////////////////////////////////////////////////////////

BaseGameEntity::~BaseGameEntity()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////
/// <summary>	Sets an identifier for the base game entity.</summary>
///
/// <remarks>	Andrew Servania,. </remarks>
///
/// <param name="val">	The value. </param>
////////////////////////////////////////////////////////////////////////////////////////////////////

void BaseGameEntity::SetID(int val)
{
	if (val >= nextValidID)
	{
		id = val;
		nextValidID++;
	}
}